// Tipos en JavaScript
const numero = 10; // Número
const texto = "Hola"; // Cadena de texto
const decimal = 3.14; // Número decimal
const flag = true; // Booleano
const bigIntValue = 1234567890123456789012345678901234567890n; // BigInt
const isSymbol = Symbol("descripcion"); // Símbolo

console.log(typeof numero); // "number"
console.log(typeof texto); // "string"
console.log(typeof decimal); // "number"
console.log(typeof flag); // "boolean"
console.log(typeof bigIntValue); // "bigint"
console.log(typeof isSymbol); // "symbol"

console.log(numero, texto, decimal, flag, bigIntValue, isSymbol);
