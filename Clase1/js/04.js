// Operadores Relacionales y Logicos
const a = 10;
const b = 10;

// Operador >
if (a > b) {
  console.log("a es mayor que b"); // a es mayor que b
} else {
  console.log("a no es mayor que b");
}

// Operador <
if (a < b) {
  console.log("a es menor que b");
} else {
  console.log("a no es menor que b"); // a no es menor que b
}

// Operador >=
if (a >= b) {
  console.log("a es mayor o igual que b");
} else {
  console.log("a no es mayor o igual que b"); // a no es mayor o igual que b
}

// Operador <=
if (a <= b) {
  console.log("a es menor o igual que b");
} else {
  console.log("a no es menor o igual que b"); // a no es menor o igual que b
}
